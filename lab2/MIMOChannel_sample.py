def MIMOChannel4x4(x):       
	from numpy import *

    
	H = 1e-2*array([[-1.5403 + 4.0193j,  -0.3531 - 0.203j,   0.27 - 0.971j,   0.18 + 0.395j],
        [ 3.7175 - 1.793j,   2.6931 + 0.179j,  -1.8730 - 0.2136j,  -0.7597 - 0.6186j],
        [0.8198 - 0.6595j,   0.7111 + 0.6545j,   0.1489 - 0.4717j,   0.3480 + 0.8001j],
        [0.0555 + 0.7715j,  -0.6328 + 1.2577j,   1.5319-0.1142j,  -0.6978+0.4267j]])
    
	shape_x = shape(x) 
    
	n = 1.2e-5*random.normal(0, 1, shape_x)+1.2e-5j*(random.normal(0, 1, shape_x))
    
	y = dot(H,x)+n
    
	return y
 
